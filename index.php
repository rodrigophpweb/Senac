                                    <p>Caso o candidato ainda não esteja de posse do diploma, é permitida a entrega do Certificado ou Declaração de Conclusão, com a data que ocorreu a colação de grau, que deve ser anterior à data de início do curso no qual o aluno está realizando a matrícula, condicionada a entrega posterior do diploma, documento necessário para a expedição do certificado de conclusão do curso de pós-graduação.</p>
                                    
                                    <p>A data de validade do Certificado ou da Declaração de Conclusão deverá ser de até, no máximo, 1 (um) ano, anterior à data da matrícula no curso.</p>
                                    <p>Para candidatos estrangeiros - apresentar cópias autenticadas dos seguintes documentos:</p>
                                    <ul>
                                        <li>Diploma de graduação, devidamente revalidado nos termos da legislação brasileira vigente;</li>
                                        <li>Registro Nacional de Estrangeiro (RNE) ou Passaporte e do visto temporário de estudante;</li>
                                        <li>Cadastro de Pessoa Física (CPF);</li>
                                        <li>Uma foto 3x4 recente (colocar o nome completo no verso);</li>
                                        <li>Declaração de proficiência em língua portuguesa, assinada por professor habilitado;</li>
                                        <li>Plano de seguro, que assegure a cobertura de despesas médico-hospitalares e despesas de repatriação, para os estrangeiros não residentes no Brasil.</li>
                                    </ul>
                                    <p>Os documentos encaminhados para a matrícula serão analisados e, sendo constatada alguma irregularidade o Centro Universitário se reserva o direito de cancelar a matrícula a qualquer tempo.</p>
                                    <p>Caso o processo de matrícula seja cancelado o candidato será comunicado por e-mail e deverá se manifestar quanto a devolução da documentação no prazo de 30 (trinta) dias corridos, a contar da data de envio do e-mail de cancelamento da matrícula. Caso não se manifeste os documentos serão destruídos.</p>
                                    <p>O candidato poderá optar pela devolução dos documentos via Correios, neste caso, deverá responder o e-mail de cancelamento do processo de matrícula e indicar o endereço de sua preferência para o recebimento da documentação e autorizar a geração de boleto bancário referente aos custos da taxa de envio.</p>
